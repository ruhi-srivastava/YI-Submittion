public class Award {
	String contractName = "";
	String contractDate = "";
	String completionDate = "";
	String awardee = "";
	String awardeeLocation = "";
	int amount = 0;

	public String getContractName() {
		return contractName;
	}

	public void setContractName(String contractName) {
		this.contractName = contractName;
	}

	public String getContractDate() {
		return contractDate;
	}

	public void setContractDate(String contractDate) {
		this.contractDate = contractDate;
	}

	public String getCompletionDate() {
		return completionDate;
	}

	public void setCompletionDate(String completionDate) {
		this.completionDate = completionDate;
	}

	public String getAwardee() {
		return awardee;
	}

	public void setAwardee(String awardee) {
		this.awardee = awardee;
	}

	public String getAwardeeLocation() {
		return awardeeLocation;
	}

	public void setAwardeeLocation(String awardeeLocation) {
		this.awardeeLocation = awardeeLocation;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

}
