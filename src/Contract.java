public class Contract {
	String contractName = "";
	String status = "";
	String bidPurchaseDeadline = "";
	String bidSubmissionDeadline = "";
	String bidOpeningDate = "";
	String tenderid = "";
	String publicationDate = "";
	String publishedIn = "";
	public String getContractName() {
		return contractName;
	}
	public void setContractName(String contractName) {
		this.contractName = contractName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getBidPurchaseDeadline() {
		return bidPurchaseDeadline;
	}
	public void setBidPurchaseDeadline(String bidPurchaseDeadline) {
		this.bidPurchaseDeadline = bidPurchaseDeadline;
	}
	public String getBidSubmissionDeadline() {
		return bidSubmissionDeadline;
	}
	public void setBidSubmissionDeadline(String bidSubmissionDeadline) {
		this.bidSubmissionDeadline = bidSubmissionDeadline;
	}
	public String getBidOpeningDate() {
		return bidOpeningDate;
	}
	public void setBidOpeningDate(String bidOpeningDate) {
		this.bidOpeningDate = bidOpeningDate;
	}
	public String getTenderid() {
		return tenderid;
	}
	public void setTenderid(String tenderid) {
		this.tenderid = tenderid;
	}
	public String getPublicationDate() {
		return publicationDate;
	}
	public void setPublicationDate(String publicationDate) {
		this.publicationDate = publicationDate;
	}
	public String getPublishedIn() {
		return publishedIn;
	}
	public void setPublishedIn(String publishedIn) {
		this.publishedIn = publishedIn;
	}

}
